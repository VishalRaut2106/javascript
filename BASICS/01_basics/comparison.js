// --- Explanatory Notes ---
// Comparison operators (>, >=, <, ==, !=, ===) are used to compare values in JavaScript.
// == compares values after type coercion, while === checks both value and data type strictly.
// Comparing undefined with numbers results in false or unexpected results due to type conversion.
// Always use === for strict comparison to avoid bugs.
//
// --- Advantages ---
// Understanding comparison operators helps prevent logic errors.
// Strict equality (===) ensures safer, more predictable code.
